# Calcular el monto de una compra de equipo informático

# Inicio: Crear las variables
producto = "Laptop"
precio_unitario = 2500
cantidad = 2

# Proceso: Realizar las operaciones de cálculo
monto_total = precio_unitario * cantidad

# Salida: Visualización de resultados
print(f"Producto: {producto}")
print(f"Precio unitario: S/{precio_unitario}")
print(f"Cantidad:{cantidad}")
print(f"Monto total a pagar: S/{monto_total}")